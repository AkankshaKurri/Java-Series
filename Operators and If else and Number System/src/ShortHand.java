public class ShortHand {
    public static void main(String[] args) {
        //Short Hand Operators
        int a= 5;
        System.out.println("Addition Assignment: " +  (a += 3));
        System.out.println("Subtraction Assignment: " + (a -= 2));
        System.out.println("Multiplication Assignment:" + (a *= 4));
        System.out.println("Division Assignment:" + (a /= 4));
        System.out.println("Remainder Assignment:" + (a %= 4));


    }
}
